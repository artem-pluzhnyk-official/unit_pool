void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	int aa;
	int i;
	int j;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	i = i - 1;
	while (j <= i)
	{
		ft_putchar(str[j]);
		j++;
	}
}
