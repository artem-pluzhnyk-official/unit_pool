int	ft_iterative_power(int nb, int power)
{
	if (power < 0)
	{
		return (0);
	}
	if (power == 0)
	{
		return (1);
	}
	if (power > 0)
	{
		while (power - 1 > 0)
		{
			nb = nb * nb;
			power--;
		}
	}
	return (nb);
}
